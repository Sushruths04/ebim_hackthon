#!/usr/bin/env bash
# One-time setup for the task2 eval container:
#   1. create the persistent artifact volume on the host
#   2. create a .env (from .env.example) with your UID/GID baked in
set -euo pipefail

SCRIPT_DIR="$(cd -- "$(dirname -- "${BASH_SOURCE[0]}")" && pwd)"
ENV_FILE="${SCRIPT_DIR}/.env"
ENV_EXAMPLE="${SCRIPT_DIR}/.env.example"

# Defaults mirror docker/.env.base.
ISAAC_DOCKER_ROOT="${ISAAC_DOCKER_ROOT:-${HOME}/docker/ebim-challenge}"
HOST_UID="${HOST_UID:-$(id -u)}"
HOST_GID="${HOST_GID:-$(id -g)}"

OUTPUT_DIR="${ISAAC_DOCKER_ROOT}/eval-task2"

echo "[setup] Creating persistent artifact volume: ${OUTPUT_DIR}/evaluate"
mkdir -p "${OUTPUT_DIR}/evaluate"

# Ensure the host dir is owned by the container user so it can write artifacts.
if [ "$(id -u)" = "0" ]; then
    chown -R "${HOST_UID}:${HOST_GID}" "${OUTPUT_DIR}"
fi

if [ -f "${ENV_FILE}" ]; then
    echo "[setup] ${ENV_FILE} already exists; leaving it untouched."
else
    echo "[setup] Writing ${ENV_FILE}"
    {
        echo "# Generated by setup.sh on $(date -Iseconds)"
        echo "ISAAC_DOCKER_ROOT=${ISAAC_DOCKER_ROOT}"
        echo "HOST_UID=${HOST_UID}"
        echo "HOST_GID=${HOST_GID}"
    } > "${ENV_FILE}"
fi

echo "[setup] Done."
echo "[setup]   Artifacts will persist under: ${OUTPUT_DIR}/evaluate"
echo "[setup]   Reference defaults:           ${ENV_EXAMPLE}"
echo "[setup] Next:"
echo "[setup]   bash ${SCRIPT_DIR}/run.sh up"
echo "[setup]   bash ${SCRIPT_DIR}/run.sh evaluate"
